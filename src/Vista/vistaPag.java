  // Muestra el gráfico en un JFrame
    public void mostrarGrafico(JFreeChart barChart) {
        // Crea un panel para el gráfico
        ChartPanel chartPanel = new ChartPanel(barChart);
        // Establece las dimensiones 
        chartPanel.setPreferredSize(new java.awt.Dimension(800, 600));
        // Crea un nuevo JFrame para el gráfico
        JFrame chartFrame = new JFrame("Ventas por Vuelo");
        //botón de cierre
        chartFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        // Agrega el panel del gráfico
        chartFrame.getContentPane().add(chartPanel, BorderLayout.CENTER);
        // Ajusta 
        chartFrame.pack();
        chartFrame.setLocationRelativeTo(null);
        chartFrame.setVisible(true);
    }

