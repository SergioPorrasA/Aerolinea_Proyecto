    // Agrega un listener a la tabla vuelos para detectar si cambio en selección de este
        private void agregarListenerTabla() {
            // Añade un ListSelectionListener a la tabla
            tablaVuelos.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
                @Override
                public void valueChanged(ListSelectionEvent event) {
                    // Verifica si la selección no está siendo ajustada y si hay una fila seleccionada en la tabla
                    if (!event.getValueIsAdjusting() && tablaVuelos.getSelectedRow() != -1) {
                        btnSiguiente.setEnabled(true);
                    }
                }
            });
        }

    private void customizeButton(JButton button) {
        // Color de fondo
        button.setBackground(new Color(60, 63, 65));
        // Color del texto
        button.setForeground(Color.WHITE);
        // Fuente y tamaño del texto
        button.setFont(new Font("Arial", Font.BOLD, 14));
        // Desactiva el resaltado cuando está enfocado
        button.setFocusPainted(false);
        // Establece un borde vacío para dar espacio alrededor del texto
        button.setBorder(BorderFactory.createEmptyBorder(10, 20, 10, 20));
    }
